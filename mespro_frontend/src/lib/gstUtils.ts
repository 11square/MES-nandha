/**
 * Get state name from GST number's first 2 digits (state code).
 * Returns empty string if invalid or not found.
 */
export function getStateFromGST(gstNumber: string): string {
  if (!gstNumber || gstNumber.length < 2) return '';
  const stateCode = gstNumber.substring(0, 2);
  return GST_STATE_CODE_MAP[stateCode] || '';
}

/**
 * Get list of districts for a given state name.
 * Returns empty array if state not found.
 */
export function getDistrictsForState(stateName: string): string[] {
  if (!stateName) return [];
  return STATE_DISTRICTS_MAP[stateName] || [];
}

/**
 * Get all Indian states (sorted).
 */
export function getAllStates(): string[] {
  const states = [...new Set(Object.values(GST_STATE_CODE_MAP))];
  return states.sort();
}
